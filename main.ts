//post and pre increment -decrement
//variable ++ (post)  ++variable(pre)
// --  (post)  --variable(pre)


let x : number = 4;
//post increment

let postincrement: number = x++;
console.log("post increment value",postincrement);
console.log("value after postincrement",x);


//pre increment
let preincrement : number = ++x;
console.log("value after preincrement",preincrement);


let postdecrement: number = x--;
console.log("post decrement value",postdecrement);
console.log("value after postdecrement",x);


//pre increment
let predecrement : number = --x;
console.log("value after predecrement",predecrement);
